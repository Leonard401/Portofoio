export function otherLanguage(lang) {
  return lang === 'ro' ? 'en' : 'ro';
}

export function getStoredLanguage(storage) {
  try {
    const store = storage || (typeof localStorage !== 'undefined' ? localStorage : null);
    return store && store.getItem('lang') === 'en' ? 'en' : 'ro';
  } catch (e) {
    return 'ro';
  }
}

export function storeLanguage(lang, storage) {
  try {
    const store = storage || (typeof localStorage !== 'undefined' ? localStorage : null);
    if (store) store.setItem('lang', lang);
  } catch (e) { /* storage unavailable (private mode, disabled cookies, etc.) */ }
}

export function applyLanguage(lang, doc) {
  const document_ = doc || (typeof document !== 'undefined' ? document : null);
  if (!document_) return;
  const dict = translations[lang] || translations.ro;
  document_.querySelectorAll('[data-i18n]').forEach((el) => {
    if (Object.prototype.hasOwnProperty.call(dict, el.dataset.i18n)) {
      el.innerHTML = dict[el.dataset.i18n];
    }
  });
  document_.documentElement.lang = lang;
}
